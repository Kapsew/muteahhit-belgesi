-- 1. Yİ-ÜFE endeksleri (TÜİK, aylık)
create table if not exists ufe_endeks (
  yil   smallint not null,
  ay    smallint not null check (ay between 1 and 12),
  deger numeric(10,2) not null,
  guncelleme timestamptz default now(),
  primary key (yil, ay)
);
create index if not exists ufe_endeks_yil_idx on ufe_endeks(yil);

-- 2. Yapı Yaklaşık Birim Maliyetleri (Bakanlık tebliği, yıllık)
-- donem örnek: "2026", "2023-2", "2022-1" (2022 ve 2023 yıllarında yıl içi revizyon var)
create table if not exists birim_maliyet (
  donem text not null,
  sinif text not null,            -- "I.A", "III.B", "V.D" vb.
  fiyat integer not null,         -- TL/m²
  guncelleme timestamptz default now(),
  primary key (donem, sinif)
);
create index if not exists birim_maliyet_donem_idx on birim_maliyet(donem);

-- 3. Grup eşikleri + ek bilgiler (Yapım Müteahhitliği Yeterlilik Şartları)
create table if not exists grup_esik (
  grup text primary key,            -- "A", "B1", "H" vb.
  sira smallint not null,           -- 1=A en üst, 14=H en alt — sıralama için
  asgari_is_deneyim bigint,         -- TL, H'de null
  azami_yapim_isi bigint,           -- TL, A'da null=sınırsız
  banka_referans bigint,            -- TL, H'de null
  toplam_ciro bigint,               -- TL, F altında null
  yapim_ciro bigint,                -- TL
  guncelleme timestamptz default now()
);

